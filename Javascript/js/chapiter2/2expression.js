var c = 3;
// 3 est une expression dont la valeur est le nombre 3
var d = c;
// c est une expression dont la valeur est cell de c (ici 3)
// (d + 1) est une expression
// sa valeur est celle de d augmentée de 1 (ici 4)
d = d + 1;
// d contient la valeur 4
console.log(d);
// la console affiche 4

