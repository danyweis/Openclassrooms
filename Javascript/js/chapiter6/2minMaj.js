var motInitial = "Bora-Bora";
console.log("Voici le mot initial " + motInitial);

var motEnMinuscule = motInitial.toLowerCase();
console.log("Voici le mot .toLowerCase() " + motEnMinuscule); // Affiche "bora-bora"

var motEnMajuscule = motInitial.toUpperCase();
console.log("Voici le mot .toUpperCase() " + motEnMajuscule); // Affiche "BORA-BORA"
